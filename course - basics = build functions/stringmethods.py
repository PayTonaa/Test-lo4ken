name = "bartek"

print(len(name)) # długosć stribngy
print(name.find("k")) # wyszukuje znak w stringu i zwraca jego idneks
print(name.capitalize()) # pierwsza litera mojego stringu jest wielka
print(name.upper()) # wszystko w stringu jest drukowane
print(name.lower()) # wsydstko w stringy jest małymi literami
print(name.isdigit()) # true / false sprawdza czy string jest znakami
print(name.isalpha()) # true/ false czy wszystko ejst literami alfabetu ( wykrywa spacje jako false bo to nie litera alfabetu )
print(name.count("o")) # liczy ile określonego znaku jest w moim stringu
print(name.replace("k","nigger")) # zamineia w stringu określony w 1 cudzysłowniu znak na inny znak podany w drugim
print(name*3) # wyswietla 3 razy
