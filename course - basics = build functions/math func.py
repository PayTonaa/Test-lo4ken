import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #zaokrąglij
print(math.ceil(pi)) # zaokrągla nie w dół ( floor ) / góre (celi)
print(math.floor(pi))  # math. odwołanie do funkcji iportowanej
print(abs(pi)) # absolutn wartość ( odległośc na osi od 0 )
print(pow(pi, 2)) # potęga tutaj pi do potęgi 2
print(math.sqrt(430))  # pierwiastek
print(max(x, y, z)) #największa wartość z posórd tych 3
print(min(x,y,z)) # najmniejsza wetosć
